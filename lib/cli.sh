p6_git_init() {

    git init "$@"
}

p6_git_checkout() {

    git checkout "$@"
}

p6_git_reset() {

    git reset "$@"
}

p6_git_commit() {

    git commit "$@"
}

p6_git_add() {

    git add "$@"
}

p6_git_status() {

    git status "$@"
}

p6_git_push() {

    git push "$@"
}

p6_git_remote() {

    git remote "$@"
}

p6_git_symbolic_ref() {

    git symbolic-ref "$@"
}

p6_git_rev_parse() {

    git rev-parse "$@"
}
