######################################################################
#<
#
# Function: p6_git_cli_push_u(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_push_u() {
    shift 0

    p6_git_cli push -u "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_push_fu()
#
#>
######################################################################
p6_git_cli_push_fu() {

    p6_git_cli_push_u -f
}
######################################################################
#<
#
# Function: p6_git_cli_push_tags(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_push_tags() {
    shift 0

    p6_git_cli push --tags "$@"
}
