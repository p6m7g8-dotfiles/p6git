# shellcheck shell=sh

######################################################################
#<
#
# Function: p6_git_cli_push_u(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_push_u() {
    shift 0

    p6_git_cli push -u "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_push_fwl(...)
#
#  Args:
#	... -
#
#>
######################################################################
p6_git_cli_push_fwl() {
    shift 0

    p6_git_cli push -u --force-with-lease --force-if-includes "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_push_fu(...)
#
#  Args:
#	... -
#
#>
######################################################################
p6_git_cli_push_fu() {
    shift 0

    p6_git_cli_push_fwl "$@"
}
######################################################################
#<
#
# Function: p6_git_cli_push_tags(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_push_tags() {
    shift 0

    p6_git_cli push --tags "$@"
}
