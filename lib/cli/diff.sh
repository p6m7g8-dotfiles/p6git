# shellcheck shell=sh

######################################################################
#<
#
# Function: p6_git_cli_diff(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_diff() {
    shift 0

    p6_git_cli diff --no-ext-diff "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_diff_previous(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_diff_previous() {
    shift 0

    p6_git_cli_diff "HEAD^" "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_diff_head(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_diff_head() {
    shift 0

    p6_git_cli_diff "HEAD" "$@"
}
