# shellcheck shell=sh

######################################################################
#<
#
# Function: p6_git_cli_rebase(...)
#
#  Args:
#	... - 
#
#>
######################################################################
p6_git_cli_rebase() {
    shift 0

    git rebase "$@"
}

######################################################################
#<
#
# Function: p6_git_cli_rebase_continue()
#
#>
######################################################################
p6_git_cli_rebase_continue() {

    p6_git_cli_rebase --continue
}

######################################################################
#<
#
# Function: p6_git_cli_rebase_abort()
#
#>
######################################################################
p6_git_cli_rebase_abort() {

    p6_git_cli_rebase --abort
}
